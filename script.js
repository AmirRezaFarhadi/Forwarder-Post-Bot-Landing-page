// تغییر زبان
function changeLanguage(lang, label) {
    const elements = document.querySelectorAll("[data-key]");
    elements.forEach(el => {
        const key = el.getAttribute("data-key");
        if (translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });
    document.querySelector(".lang-text").textContent = label;
}

// پیش‌فرض
changeLanguage("en", "EN");

// باز/بسته شدن
const langDropdown = document.querySelector('.lang-dropdown');
const langBtn = langDropdown.querySelector('.lang-btn');
const langItems = langDropdown.querySelectorAll('.lang-menu li');

langBtn.addEventListener('click', () => {
    langDropdown.classList.toggle('open');
});

langItems.forEach(item => {
    item.addEventListener('click', () => {
        const lang = item.getAttribute("data-lang");
        changeLanguage(lang, item.textContent);
        langDropdown.classList.remove('open');
    });

    
});
